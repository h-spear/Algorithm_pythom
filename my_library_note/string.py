# lower() : 문자열을 소문자로
# upper() : 문자열을 대문자로
# isalpha() : 문자열이 문자로만 되어있는지 확인 후 True, False로 리턴(공백문자, ':'등 안됨)
# isalnum() : 문자열이 영어, 한글 혹은 숫자로만 되어있는지 확인 후 True, False로 리턴(공백문자, ':'등 안됨)
# isdigit() : 문자열이 숫자인지 확인 후 True, False로 리턴
# isnumeric() : 숫자처럼 생긴 글자 모두 판별(제곱근 및 분수, 거듭제곱 특수문자까지)
# isdecimal() : 문자열이 int 타입으로 변환 가능한 문자로 구성됐을 때 true 리턴

# sorted() : 문자열을 정렬하지만 리스트 형태로 return
# sort() : 제자리 정렬 메소드, 별도의 추가 공간이 필요하지 않고, return 값이 없음

# str[::-1] : 슬라이싱으로 문자열/배열 뒤집기


